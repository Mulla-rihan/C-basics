#include<stdio.h>

int main()
{
  int num1,num2;
  printf("Enter first number =");
  scanf("%d",&num1);
  printf("Enter second number =");
  scanf("%d",&num2);
  printf("Add of two numbers :%d\n",num1+num2);
  printf("Sub of two numbers :%d\n",num1-num2);
  printf("Mul of two numbers :%d\n",num1*num2);
  printf("Div of two numbers :%d",num1/num2);
  return 0;
}
